// баг · Почему Map ведёт себя не так?
// Разработчик использует объект как ключ в Map. Объясни, что происходит, и исправь код
// так чтобы он работал правильно.

const cache = new Map();
function fetchUser(id) {

    // обьект - ссылочный тип, каждый раз при вызове функции 
    //  констится новый в оперативку, поэтому мап его "не узнает". надо просто перестать использовать обьект
  const key = id ;
  if (cache.has(key)) {
    return cache.get(key); // никогда не попадает сюда
  }
  const result = `User #${id}`;
  cache.set(key, result);
  return result;
}
